    private void OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenActionPerformed
        // TODO add your handling code here:
        int returnVal = fileChooser.showOpenDialog(this);
        if(returnVal==JFileChooser.APPROVE_OPTION)  {
            File file = fileChooser.getSelectedFile();
            File processedFile = null;
            try {
                InputStream inStr = new FileInputStream(file);
                OutputStream encText = new FileOutputStream("encryptedText.txt");
                OutputStream decText = new FileOutputStream("decryptedText.txt");
                textarea.read(new FileReader(file.getAbsolutePath()), null);
                if (var) {
                Enkripsi.enkripsi(inStr, encText);
                }
                else
                Dekripsi.dekripsi(inStr, decText);
            } catch (IOException ex)    {
                System.out.println("File tidak dapat diakses pada"+file.getAbsolutePath());
            }
        } else  {
            System.out.println("Akses file dibatalkan");
        }
    }//GEN-LAST:event_OpenActionPerformed

